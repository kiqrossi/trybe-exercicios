let weekDay = 'domingo';

if (weekDay === 'segunda-feira' || weekDay === 'quarta-feira' || weekDay === 'sexta-feira') {
    console.log('Dia de Reunião!')
}

else if (weekDay === 'terça-feira' || weekDay === 'quinta-feira') {
    console.log('Dia de Futebol!')
}

else if (weekDay === 'sábado' || weekDay === 'domingo') {
    console.log('Descansou xD')
}